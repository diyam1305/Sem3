public class C
{
	public static void main(String[] args)
	{
		int a=10, b=20;
		System.out.println("Maximum number is:" + Math.max(a,b));
		System.out.println("Minimum number is:" + Math.min(a,b));
		System.out.println("Random number is:" + Math.random());
		System.out.println("Power of number is:" + Math.pow(2,4));
		System.out.println("Square root of number is:" + Math.sqrt(16));
		System.out.println("Rounded number is:" + Math.round(5.6));
		System.out.println("Ceiled number is:" + Math.ceil(5.1));
		System.out.println("Floored number is:" + Math.floor(5.9));
		System.out.println("Absolute number is:" + Math.abs(-1));
	}
}